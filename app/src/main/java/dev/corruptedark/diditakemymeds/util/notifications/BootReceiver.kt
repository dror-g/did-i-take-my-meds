package dev.corruptedark.diditakemymeds.util.notifications

import android.content.BroadcastReceiver
import android.content.Context
import android.content.Intent
import kotlinx.coroutines.CoroutineScope
import kotlinx.coroutines.Dispatchers
import kotlinx.coroutines.launch

/**
 * Exported receiver that handles system broadcasts (boot completed, package replaced).
 * System broadcasts require the receiver to be exported, so this is split from
 * [ActionReceiver] which handles only internal custom broadcasts.
 */
class BootReceiver : BroadcastReceiver() {

    override fun onReceive(context: Context, intent: Intent) {
        val action = intent.action
        if (action == Intent.ACTION_BOOT_COMPLETED
            || action == Intent.ACTION_LOCKED_BOOT_COMPLETED
            || action == Intent.ACTION_MY_PACKAGE_REPLACED
        ) {
            val pendingResult = goAsync()
            CoroutineScope(Dispatchers.Default).launch {
                try {
                    ActionReceiver.onStartEverything(context)
                } finally {
                    pendingResult.finish()
                }
            }
        }
    }
}
